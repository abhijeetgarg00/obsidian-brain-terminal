/**
 * Reads all files from starter/ and generates src/starter-pack.ts
 * as an embedded TypeScript array — no network required at install time.
 *
 * updateStrategy per file:
 *   "skip"   — never overwrite (user owns this file) [default]
 *   "append" — on plugin update, append new sections to existing file
 *   "bridge" — bridge files: copy only if not present, never update
 *   "never"  — vault-structure.md: never touch after first install
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const starterDir = path.join(__dirname, "..", "starter");
const outFile    = path.join(__dirname, "..", "src", "starter-pack.ts");

/** Determine update strategy based on file path */
function updateStrategy(relPath) {
  const p = relPath.replace(/\\/g, "/");

  // vault-structure.md — user's real vault map, NEVER touch after install
  if (p.includes("vault-structure.md"))          return "never";

  // AGENT.md files — bridge files, copy once, version-bump on update
  if (p === "CLAUDE.md")                         return "bridge";
  if (p === "AGENT.md")                          return "bridge";  // vault root — Devin reads this
  if (p === ".brain/AGENT.md")                   return "bridge";
  if (p === ".agents/AGENT.md")                  return "bridge";

  // Profiles — append new sections on update, never overwrite existing content
  if (p.startsWith(".brain/profiles/"))          return "append";

  // note-format.md — append new note types on update
  if (p.includes("note-format.md"))              return "append";

  // Templates — copy once only, never overwrite user's templates
  if (p.startsWith("_templates/"))               return "skip";

  // AI Profiles — copy once only
  if (p.startsWith("AI Profiles/"))              return "skip";

  // Everything else — copy once, skip if exists
  return "skip";
}

function walk(dir, base = "") {
  const entries = [];
  for (const name of fs.readdirSync(dir)) {
    if (name === ".gitkeep") continue;
    const full = path.join(dir, name);
    const rel  = base ? `${base}/${name}` : name;
    if (fs.statSync(full).isDirectory()) {
      entries.push(...walk(full, rel));
    } else {
      entries.push({
        path:           rel,
        content:        fs.readFileSync(full, "utf8"),
        updateStrategy: updateStrategy(rel),
      });
    }
  }
  return entries;
}

const files = fs.existsSync(starterDir) ? walk(starterDir) : [];

const ts = `// AUTO-GENERATED by scripts/bundle-starter.mjs — do not edit by hand
export const STARTER_PACK: { path: string; content: string; updateStrategy: string }[] = ${JSON.stringify(files, null, 2)};
export const STARTER_PACK_VERSION = "1.0";
`;

fs.writeFileSync(outFile, ts, "utf8");
console.log(`[bundle-starter] bundled ${files.length} files → src/starter-pack.ts`);
console.log(`  bridge:  ${files.filter(f=>f.updateStrategy==="bridge").length} files`);
console.log(`  append:  ${files.filter(f=>f.updateStrategy==="append").length} files`);
console.log(`  skip:    ${files.filter(f=>f.updateStrategy==="skip").length} files`);
console.log(`  never:   ${files.filter(f=>f.updateStrategy==="never").length} files`);
